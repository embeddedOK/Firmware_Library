/* DS18B20 1WIRE COMMANDS*/
#define DS18B20_CMD_SEARCH_ROM 			0xF0
#define DS18B20_CMD_READ_ROM			0x33
#define DS18B20_CMD_MATCH_ROM			0x55
#define DS18B20_CMD_SKIP_ROM			0xCC
#define DS18B20_CMD_ALARM_SEARCH		0xEC
#define DS18B20_CMD_CONVERT_T			0x44
#define DS18B20_CMD_WRITE_SCRATCHPAD	0x4E
#define DS18B20_CMD_READ_SCRATCHPAD		0xBE
#define DS18B20_CMD_COPY_SCRATCHPAD		0x48
#define DS18B20_CMD_RECALL_EE			0xB8
#define DS18B20_CMD_READ_POWER_SUPPLY	0xB4

/* DS18B20 64BIT ROM CODE BREAKDOWN	*/
#define DS18B20_ROMCODE_CRC_OFFSET		56
#define DS18B20_ROMCODE_CRC_MASK		0xFF

#define DS18B20_ROMCODE_SERIAL_NUMBER_OFFSET 8
#define DS18B20_ROMCODE_SERIAL_NUMBER_MASK   0xFFFFFFFFFFFF

#define DS18B20_ROMCODE_FAMILY_CODE_OFFSET 0
#define DS18B20_ROMCODE_FAMILY_CODE_MASK   0xFF

/* DS18B20 8BIT CONFIG REG RESOLUTION SETTINGS	*/
#define DS18B20_CONFIG_REG_9BITS 		0x1F
#define DS18B20_CONFIG_REG_10BITS 		0x3F
#define DS18B20_CONFIG_REG_11BITS 		0x5F
#define DS18B20_CONFIG_REG_12BITS 		0x7F

/* DS18B20 MIN RESOLUTION CONVERT TIMES IN MS	*/
#define DS18B20_CONFIG_9BITS_TCONV_MS	0x5E
#define DS18B20_CONFIG_10BITS_TCONV_MS	0xBC
#define DS18B20_CONFIG_11BITS_TCONV_MS	0x177
#define DS18B20_CONFIG_12BITS_TCONV_MS	0x2EE

/* DS18B20 SCRATCHPAD BYTES	*/
/* CRC POLY is X8+X5+X4+1	*/
#define DS18B20_SCRATCHPAD_BYTE_SIZE    0x09

#define DS18B20_SCRATCHPAD_TEMPLSB		0x00
#define DS18B20_SCRATCHPAD_TEMPMSB		0x01
#define DS18B20_SCRATCHPAD_TH			0x02
#define DS18B20_SCRATCHPAD_TL			0x03
#define DS18B20_SCRATCHPAD_CONFIG_REG	0x04
#define DS18B20_SCRATCHPAD_CRC			0x08

/* DS18B20 1WIRE TIMINGS	*/
#define DS18B20_RESET_PULSE_WIDTH_MIN_US 		480	/* Minimum 480us period for MASTER reset pulse and SLAVE presence pulse*/
#define DS18B20_PRESENCE_PULSE_DELAY_MAX_US 	60 	/* Max 60us SLAVE delay after reset pulse for presence pulse	*/
#define DS18B20_PRESENCE_PULSE_WIDTH_MIN_US 	60 	/* Min 60us SLAVE presence pulse	*/

#define DS18B20_TIME_SLOT_MIN_US 		60 	/* Min 60us time slot for read or write duration	*/
#define DS18B20_RECOVERY_US				1 	/* 1us bus recovery time between time slots	*/
