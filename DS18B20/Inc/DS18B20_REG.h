
#define DS18B20_MAX_DEVICES				10	//Maximum number of DS18B20 Sensors
#define DS18B20_CMD_BIT_SIZE			8
#define DS18B20_ROM_CODE_BIT_SIZE		64
#define DS18B20_ADDRESS_BIT_SIZE		56
#define DS18B20_CRC_BIT_SIZE			8

#define DS18B20_SCRATCHPAD_BYTE_SIZE    	9
#define DS18B20_SCRATCHPAD_DATA_BYTE_SIZE   8
#define DS18B20_TEMPERATURE_BIT_SIZE		16
#define DS18B20_TH_REG_BIT_SIZE				8
#define DS18B20_TL_REG_BIT_SIZE				8
#define DS18B20_CONFIG_REG_BIT_SIZE			8

/* DS18B20 1WIRE COMMANDS*/
#define DS18B20_CMD_SEARCH_ROM 			0xF0
#define DS18B20_CMD_READ_ROM			0x33
#define DS18B20_CMD_MATCH_ROM			0x55
#define DS18B20_CMD_SKIP_ROM			0xCC
#define DS18B20_CMD_ALARM_SEARCH		0xEC
#define DS18B20_CMD_CONVERT_T			0x44
#define DS18B20_CMD_WRITE_SCRATCHPAD	0x4E
#define DS18B20_CMD_READ_SCRATCHPAD		0xBE
#define DS18B20_CMD_COPY_SCRATCHPAD		0x48
#define DS18B20_CMD_RECALL_EE			0xB8
#define DS18B20_CMD_READ_POWER_SUPPLY	0xB4

/* DS18B20 64BIT ROM CODE BREAKDOWN	*/
#define DS18B20_ROMCODE_CRC_OFFSET		56
#define DS18B20_ROMCODE_CRC_MASK		0xFF

#define DS18B20_ROMCODE_SERIAL_NUMBER_OFFSET 8
#define DS18B20_ROMCODE_SERIAL_NUMBER_MASK   0xFFFFFFFFFFFF

#define DS18B20_ROMCODE_FAMILY_CODE_OFFSET 0
#define DS18B20_ROMCODE_FAMILY_CODE_MASK   0xFF

/* DS18B20 CRC Poly x^8+x^5+x^4+1 */
#define DS18B20_CRC_POLYNOMIAL 			0b100110001//((x<<8)+(1<<5)+(1<<4)+1)
#define DS18B20_CRC_POLYNOMIAL_MASK 	0x1FF

/* DS18B20 8BIT CONFIG REG RESOLUTION SETTINGS	*/
#define DS18B20_CONFIG_REG_OFFSET 		5
#define DS18B20_CONFIG_REG_MASK			0x3

/* DS18B20 MIN RESOLUTION CONVERT TIMES IN MS	*/
#define DS18B20_CONFIG_9BITS_TCONV_MS	0x5E
#define DS18B20_CONFIG_10BITS_TCONV_MS	0xBC
#define DS18B20_CONFIG_11BITS_TCONV_MS	0x177
#define DS18B20_CONFIG_12BITS_TCONV_MS	0x2EE

/* DS18B20 Resolution Bit Masks */
#define DS18B20_CONFIG_9BITS_BITMASK 	0xFFF8
#define DS18B20_CONFIG_10BITS_BITMASK	0xFFFC
#define DS18B20_CONFIG_11BITS_BITMASK 	0xFFFE
#define DS18B20_CONFIG_12BITS_BITMASK 	0xFFFF

#define DS18B20_TEMPERATURE_WHOLE_OFFSET	0x4
#define DS18B20_TEMPERATURE_WHOLE_MASK 		0xFF

#define DS18B20_TEMPERATURE_DECIMAL_OFFSET	0x0
#define DS18B20_TEMPERATURE_DECIMAL_MASK 	0x0F
#define DS18B20_TEMPERATURE_DECIMAL_LSB		625

/* DS18B20 SCRATCHPAD BYTES	*/
#define DS18B20_SCRATCHPAD_TEMPLSB		0x00
#define DS18B20_SCRATCHPAD_TEMPMSB		0x01
#define DS18B20_SCRATCHPAD_TH			0x02
#define DS18B20_SCRATCHPAD_TL			0x03
#define DS18B20_SCRATCHPAD_CONFIG_REG	0x04
#define DS18B20_SCRATCHPAD_CRC			0x08

/* DS18B20 1WIRE Bus Power */
#define DS18B20_POWER_PARASITE 0
#define DS18B20_POWER_EXTERNAL 1

/* DS18B20 1WIRE TIMINGS	*/
#define DS18B20_RESET_PULSE_WIDTH_MIN_US 		480	/* Minimum 480us period for MASTER reset pulse and SLAVE presence pulse*/
#define DS18B20_PRESENCE_PULSE_WAITS_MAX_US 	60 	/* Max 60us SLAVE delay after reset pulse for presence pulse	*/
#define DS18B20_PRESENCE_PULSE_RESAMPLE_US 		15 	/* Resample SLAVE presence pulse after 15us	*/

#define DS18B20_START_OF_SLOT_MIN_US 			1 /* 1uS minimum start of slot pulse, NOTE set to 2 to ensure min is met!*/
#define DS18B20_TIME_SLOT_MIN_US 				60 	/* Min 60us time slot for read or write duration	*/
#define DS18B20_SLOT_RECOVERY_US				5//DEBUG 1 	/* 1us bus recovery time between time slots	*/
#define SD18B20_MASTER_READ_SLOT_US				15	/* Master should sample bus 15us after issuing a slot, subtract the slot issue time*/
